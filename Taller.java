
package Taller1;
/**
 * Interface que permite aplicar algunas funciones sobre los
   objetos creados de una clase
 * @author edwin
 */
public interface Taller {
    /**
     * Cambia el estado del tanque de combustible de la nave
     */
    public void tanquear();
    /**
     * Cambia el estado de la variable que define si ya se realizo
       el mantenimiento requerido antes de un vuelo
     */
    public void mantenimiento();    
}
